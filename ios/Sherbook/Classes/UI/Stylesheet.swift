import UIKit

extension UIColor {
    class var emerald: UIColor { return UIColor(forHex: "00a855") }
    class var emeraldHighlighted: UIColor { return UIColor(forHex: "00743B") }
    class var warmGrey: UIColor { return UIColor(forHex: "a2a2a2") }
    class var silver: UIColor { return UIColor(forHex: "dddee2") }
    class var greyish: UIColor { return UIColor(forHex: "b2b2b2") }
    class var almostBlack: UIColor { return UIColor(forHex: "172215") }

    class var white95: UIColor { return UIColor(forHex: "fafafa").withAlphaComponent(0.95) }
}
